	<p><strong>Rental Guide Singapore – Going for a Home Viewing</strong></p><p><strong><br></strong></p><p><img src="/blog/media/wyswyg/1/fb2c23ddfe-room-edited.png" alt="" style="display: block; margin: auto;"></p>  <p>You saw the pictures of the home in the property listing, and the place looked fabulous. The location seemed perfect, and the price was reasonable.</p>  <p>The time has come for the home viewing, and you are almost ready to part with your money for the deposit and rent the place. But WAIT. Here are some tips on what to look out for before you commit.</p>  <table>  <tbody><tr>   <td>   <p><strong> </strong></p>   <ul><li><strong></strong><strong>Research, research, research</strong></li></ul><p>Moving into a new place also means moving into the neighbourhood. Therefore, it is important to find out more about the location of the house and explore the area.</p><p>For example, it is worthwhile to find out if there is convenient access to public transport if that is your choice of commute on a daily basis.</p><p>Depending on your lifestyle needs and preferences, it is also good to check out whether there are amenities such as a supermarket (for grocery shopping) or exercise facilities (for the fitness junkies).</p><p>These are factors that will help you to settle in quickly and comfortably, so it is good to do some research.</p><ul><li><strong></strong><strong>Check, and check again</strong></li></ul><strong></strong><p><img src="/blog/media/wyswyg/1/091784fe1c-viewing-checklist-edited.png" alt="" style="display: block; margin: auto;"></p>
<p>Granted, a house viewing does not really give you the freedom of time to scrutinise every tiny detail. But with a little preparatory work, you can come up with a checklist of non-negotiables items and use that as a gauge whilst checking.</p><p>Here’s a sample checklist:</p><ul><li>Condition of room/house<ul><li>Any visible defects?</li><li>Any signs of insects/infestation?</li><li>Are the lights/air-conditioning working?</li></ul></li><li>Condition of Bathroom/Shower facilities<ul><li>Does the bathroom have heated showers?</li><li>Is there mould in the bathroom?</li></ul></li><li>Facilities<ul><li>Where are the powerpoints located? Are they easy to access?</li><li>Is Wifi available?</li><li>Does sunlight shine in through the windows during the afternoon? <i>(Singapore can be hot)</i></li><li>How is the laundry area?</li></ul></li></ul><ul><li><strong></strong><strong>Communicate </strong></li></ul><strong></strong><img src="/blog/media/wyswyg/1/059e0fe39d-person-woman-smartphone-calling-edited.png" alt="" style="display: block; margin: auto;"><br>      <p>The home viewing session is a useful opportunity for you to communicate any concerns or enquiries you might have. For example, it would be good to be on the same page regarding issues like paying for utilities, usage of furniture, maintenance etc. This will help greatly in preventing disputes with your landlord which might arise later on.<br></p>      </td>  </tr> </tbody></table>    <p>There you go, three simple tips to help you maximise your home viewing experience, and take you closer to finding the ideal place to rent and call home. Look out for more of our tips and guides on our blog!</p><hr>  <p><i>Need some help? OneHome Property provides a one-time free-of-charge home inspection + free Handyman services for a 30-day period, to help with minor maintenance and repairs for all landlords and tenants who transact through us. For more information, visit </i><a href="http://onehome.sg/our-services/handyman-serv